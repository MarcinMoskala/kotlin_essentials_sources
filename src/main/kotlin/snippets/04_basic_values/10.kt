package f_04_basic_values.s_10

fun main() {
    println(1 % 3) // 1
    println(2 % 3) // 2
    println(3 % 3) // 0
    println(4 % 3) // 1
    println(5 % 3) // 2
    println(6 % 3) // 0
    println(7 % 3) // 1
    println(0 % 3) // 0
    println(-1 % 3) // -1
    println(-2 % 3) // -2
    println(-3 % 3) // 0
}
