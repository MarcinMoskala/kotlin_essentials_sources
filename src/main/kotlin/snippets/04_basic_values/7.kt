package f_04_basic_values.s_7

fun main() {
    val i1 = 12
    val i2 = 34
    println(i1 + i2) // 46
    println(i1 - i2) // -22
    println(i1 * i2) // 408
    println(i1 / i2) // 0

    val d1 = 1.4
    val d2 = 2.5
    println(d1 + d2) // 3.9
    println(d1 - d2) // -1.1
    println(d1 * d2) // 3.5
    println(d1 / d2) // 0.5599999999999999
}
